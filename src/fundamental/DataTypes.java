package fundamental;

public class DataTypes {
	public static void main(String[] args) {
		boolean b = true;// value of boolean true or false
		byte by = 127; // value range -128 .. 127
		short s = 2345;// value range -32,768 .. 32,767
		int i = 123;// value range -2,147,483,648 .. 2,147,483,647
		long l = 00;// value range -9,223,372,036,854,775,808 ..
					// 9,223,372,036,854,775,807
		float f = 23f;// value range 3.40282347 x 1038, 1.40239846 x 10-45
		double d = 5.1;// value range 1.7976931348623157 x 10308,
						// 4.9406564584124654 x 10-324

		System.out.println(b);
		System.out.println(by);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);

	}
}
