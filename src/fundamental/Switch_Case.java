package fundamental;

public class Switch_Case {
	public static void main(String[] args) {
		int i = 5;
		switch (i) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
		case 4:
			System.out.println("4");
			break;
		case 5:
			System.out.println("5");
			break;
		default:
			System.out.println("Not in 1, 2, 3, 4 or 5");
		}
	}
}
