package string;

public class Case_Of_String {

	public static void main(String[] args) {
		String name = "My name is Arun Maharana";
		System.out.println(name);

		// change all letters to lower case
		String lowerCaseString = name.toLowerCase();
		System.out.println(lowerCaseString);// my name is arun maharana

		// change all letters to upper case
		String upperCaseString = name.toUpperCase();
		System.out.println(upperCaseString);// MY NAME IS ARUN MAHARANA

	}

}
